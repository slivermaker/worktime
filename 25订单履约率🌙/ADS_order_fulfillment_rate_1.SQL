    C.PERIOD,
    C.PRODUCTION_LINE,
    C.FACTORY,
    C.DELAY_CNT,  
    SUM(C.DELAY_CNT) OVER (
        PARTITION BY C.PRODUCTION_LINE, C.FACTORY, EXTRACT(YEAR FROM C.PERIOD) 
        ORDER BY C.PERIOD
        ROWS BETWEEN UNBOUNDED PRECEDING AND CURRENT ROW
    ) AS ASS_DELAY_CNT,  
    C.DELAY_3_6_PRODUCTION,
    C.DELAY_OVER_7_PRODUCTION,
    C.DELAY_3_6_NON_PRODUCTION,
